package model;

public class NodeCoordinate
{
	private double posX;
	private double posY;

	public NodeCoordinate() {}

	public double getPosX()
	{
		return posX;
	}

	public void setPosX(double posX)
	{
		this.posX = posX;
	}

	public double getPosY()
	{
		return posY;
	}

	public void setPosY(double posY)
	{
		this.posY = posY;
	}


}
